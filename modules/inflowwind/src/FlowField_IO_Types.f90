     ALLOCATE( IntKiBuf(  Int_BufSz  ), STAT=ErrStat2 )
     IF (ErrStat2 /= 0) THEN 
       CALL SetErrStat(ErrID_Fatal, 'Error allocating IntKiBuf.', ErrStat, ErrMsg,RoutineName)
       RETURN
     END IF
  END IF
  IF(OnlySize) RETURN ! return early if only trying to allocate buffers (not pack them)

  Re_Xferred  = 1
  Db_Xferred  = 1
  Int_Xferred = 1

  IF ( .NOT. ALLOCATED(InData%Position) ) THEN
    IntKiBuf( Int_Xferred ) = 0
    Int_Xferred = Int_Xferred + 1
  ELSE
    IntKiBuf( Int_Xferred ) = 1
    Int_Xferred = Int_Xferred + 1
    IntKiBuf( Int_Xferred    ) = LBOUND(InData%Position,1)
    IntKiBuf( Int_Xferred + 1) = UBOUND(InData%Position,1)
    Int_Xferred = Int_Xferred + 2
    IntKiBuf( Int_Xferred    ) = LBOUND(InData%Position,2)
    IntKiBuf( Int_Xferred + 1) = UBOUND(InData%Position,2)
    Int_Xferred = Int_Xferred + 2

      DO i2 = LBOUND(InData%Position,2), UBOUND(InData%Position,2)
        DO i1 = LBOUND(InData%Position,1), UBOUND(InData%Position,1)
          ReKiBuf(Re_Xferred) = InData%Position(i1,i2)
          Re_Xferred = Re_Xferred + 1
        END DO
      END DO
